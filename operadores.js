//operadores comparacao
console.log("maior que", ">", 3 > 2);
console.log("menor que", "<", 3 < 2);
console.log("maior ou igual que", ">=", 3 >= 2);
console.log("menor ou igual que", "<=", 3 <= 2);
console.log("igualdade", "==", "5" == 5);
console.log("igualdade restrita", "===", "5" === 5);
console.log("diferente", "!=", 5 != 4);

